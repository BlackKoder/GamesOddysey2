                <p>Hay que tener en cuenta que es cierto que el que sigan saliendo nuestros juegos favoritos depende de la aceptación y la ganancia que obtengan los desarrolladores de ese proyecto, y que el deber ser sería que si te gusta el producto lo pagues para que puedan seguir ofreciendote mejores cosas. Pero, hay cosas que realmente son un poco exageradas. Para qué es necesario hacer un juego con un gran nivel de detalle, mapas extensos en mundo abierto, miles de horas de juegos buscando objetos con más de 100 minijuegos si al final hay juegos que están repletos de bugs y mecánicas que no están pulidas.</p>

