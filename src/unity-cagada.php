            Esto al final ha ocasionado una gran polémica y revuelo en todo el internet, ya que no solamente se ven afectados los editores de todos esto, los mismos desarrolladores de videojuegos y nosotros mismos como jugadores, consumidores de videojuegos nos vemos muy afectados, porque muchos juegos famosos e importantes para muchos es posible que los retiren antes de que estas políticas entren en vigencia a partir del 1 enero del 2024, por lo que ya no se podrán disfrutar a menos a que los desarrolladores decidan hacer algo, porteando sus juegos por ejemplo, a otro motor gráfico... Cosa que es muy difícil y en el caso de algunos juegos que el proyecto está muy avanzado es sinceramente imposible.
