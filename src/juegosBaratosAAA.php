                <p>Pienso que las industrias podrían hacer juegos un poco más pequeños o que requieran de menos trabajo pero manteniendo el mismo enfoque de sus obras, y no sería necesario elevar tanto los costos de producción. Ya que esto tarde o temprano será un lujo para las personas y no hay que ver en sí que un juego es un lujo, un juego es cultura, debería de tener un precio accesible para todos.</p>

