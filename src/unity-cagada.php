            <h2 class="subtitulo">¿Realmente es mejor usar Godot?</h2>  

            <img src="./assets/images/godot.jpg" loading="lazy">  

            <p>Bueno, usar Godot es algo que se ha hecho un poco tendencia para estos últimos años, en vista de que el motor es open source y que ofrece una buena utilidad de herramientas que pueden ayudar mucho con el desarrollo de juegos en 2D. Sin embargo, tiene sus complicaciones, igualmente a pesar de que Godot está en auge, no tiene una base tan sólida como la que tiene Unity que ha conseguido durante años, sin embargo, los fundadores del Engine, se comprometen a mejorar cada vez más. El único punto no tan a favor es que es comenzar casi de cero, y en Unity estaban la mayoría de assets que se podían utilizar para ayudar en el trabajo. Quizás Godot y todo lo que tiene que ver con el open source nos dé más libertad, pero el precio que tendremos que pagar es la comodidad.</p>

            <h2 class="subtitulo">¿Quién fue el genio detrás de todo esto?</h2>

              <img src="./assets/images/puto.png">

                <p>Se le debe semejante hazaña a John Riccitiello CEO de Unity desde el año 2014, y ex CEO de Electronic Arts desde 1997 hasta el 2014. Muchos especulan, que la posible razón por la que Riccitiello dejó de trabajar en EA fue por su sistema de microtransacciones que quiso implementar en Battlefield 3, donde si se te acababan las balas, por recargar munición en media partida, tendrías que pagar para poder recargar el arma. Se podrán imaginar que tipo de locura es está dentro de un juego shooter.</p>

            <h2 class="subtitulo">Las aclaraciones de Unity</h2>    

                <p>Ellos buscaron de rectificar disculpándose a través de su cuenta de Twitter diciendo que esto no le iba a afectar a la gran mayoría, sino a los desarrolladores con muchas ventas. Pero honestamente las matemáticas no cuadran de ninguna forma con la fórmula que ellos plantean, no planean echarse para atrás con las tarifas. Pero si piensan cambiar las cosas que habían planteado. En Unity dicen una cosa y después dicen otra, habiendo borrado incluso sin previo aviso sus anteriores términos y condiciones del 2019 almacenados en GitHub.
                </p>

            <h2 class="subtitulo">¿Es posible que Silk Song nunca salga a la luz?</h2>

            <img src="./assets/images/silksong.jpeg">

            <p>Es cierto que desde que anunciaron el lanzamiento de Silk Song, este se ha retrasado demasiado y al final pospusieron su fecha de lanzamiento hasta nuevo aviso, lo que nos da a entender que Team Cherry ha tenido problemas con el desarrollo de Silk Song a pesar de ser un DLC de Hollow Knight, ahora con la pérdida que los va a hacer tener Unity, hacer un porteo de gran parte del desarrollo de Silk Song, y rehacer todo el juego de Hollow Knight en otra plataforma es un gran salto hacia atrás para este buen equipo de desarrollo. Esto no sienta nada bien, y posiblemente Silk Song tarde muchísimo tiempo más en su lanzamiento gracias a esta locura, pero siendo honesto tengo la sensación y la esperanza de que Team Cherry no se va a rendir y nos va a traer ese tan esperado Silk Song que muchos deseamos.</p>

            <p>Sinceramente, esto no tiene ni pies ni cabeza, y la mejor solución sería usar otro engine y no seguir usando Unity, para que esto quede como una advertencia para esta y muchas otras empresas, este tipo de cosas son las que pasan por únicamente pensar en el dinero, y no pensar en el consumidor, en el desarrollador. Esto nos afecta a todos los que estamos en ese rubro y sinceramente es para reflexionar.</p>
                 
