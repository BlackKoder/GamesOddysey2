            <h1>Mario Sunshine una joya infravalorada</h1>


            <p>Super Mario Sunshine, una estrella que nunca pudo deslumbrar en un mundo con una era oscura. Eso es lo que podría definir a Mario Sunshine para ese entonces, un juego que muchos no supieron apreciar para su lanzamiento, porque al final decían que era una mala secuela de su predecesor, el Mario 64. Pero realmente Super Mario Sunshine, era un juego tan malo o realmente fue una joya oculta?.
          </p>

            <h2 class="subtitulo">Origenes</h2>

            <img src="./assets/images/Mario64_2.jpg" loading="lazy" alt="Link frente al gran árbol deku con la espada y escudo kokiri" title="Link frente al gran árbol deku con la espada y escudo kokiri">

            <p>Se había pensado en una secuela de la gran aclamada entrega de Super Mario 64, juego que en 1996 causó un gran revuelo por sus mecánicas, arte y jugabilidad. Un pionero de su época, la rentabilidad que tuvo Nintendo gracias a la venta de Mario 64 era muy grande, por eso decidieron hacer una esperada secuela de este mismo título. con el nombre clave de Super Mario 128, haciendo referencia a los 128 bits de la consola, para el año 1997 estaban en el desarrollo de este juego y se canceló, luego en el año 2000 se hizo una demostración gráfica de lo que podía ser capaz la consola de Nintendo, la gamecube para ser específicos, de poder manejar 128 marios, cada uno con una animación independiente distinta y obviamente con gráficos en 3D. A muchos no les gustó la idea de que esto fuera la secuela de Super Mario 64, y por eso Nintendo lo terminó descartando e innovando con Super Mario Sunshine.
            </p>


            <h2 class="subtitulo">La época oscura de Nintendo</h2>

            <img src="./assets/images/MarioEdgy.png">

            <p>Nintendo sabía que la única forma de causar un revuelo y quedar en la mente de la gente con cualquier juego que sacaran era haciendo juegos distintos, juegos con funcionalidades y mecánicas que no hayamos visto antes y sean innovadoras con lo que nos están ofreciendo como concepto. Es por eso que al desarrollar Mario Sunshine pensaron en todo, desde poner una historia con una narrativa un poco más elaborada, como el mejorar los gráficos y exprimir al máximo las mecánicas que le ofrecía la narrativa de ese mundo. </p>

            <p>Nintendo estaba pasando por una etapa oscura, ya que el público infantil al que ellos estaban acostumbrados a hacer juegos fueron pasando por la etapa de la adolescencia o bien entrando a la adultez, y a muchos ya no les atraían los títulos que ofrecía Nintendo, al contrario de Sega que hacía cosas más llamativas para ese tipo de público, o cuando se anunció la PlayStation y esta ofrecía algo que los demás contrincantes en la guerra de consolas no disponía.</p>

            <img src="./assets/images/SegaComercial.jpg">

            <p>Nintendo tuvo muchas críticas para ese entonces, ya que el público que tenían no querían que innovaran solo querían secuelas con mejores gráficos y más niveles. Pero al final no querían nada nuevo, y así no es Nintendo, obviamente iban a buscar la forma de ofrecer algo distinto. La mayoría de juegos que salieron para esa época sufrieron una infinidad de críticas como el Zelda Wind Waker, que pensaban que tenía una estética demasiado infantil y la gente quería un Ocarina of Time 2 con mejores gráficos, pasó con el Star fox adventure porque decían que no era un juego de Starfox, se alejaba mucho de lo que habían mostrado antes, Igual con Metroid y muchos otros juegos más.</p>

            <p>Si es cierto que Mario no fue un juego que de hecho hayan críticado mucho para esa época de Nintendo, pero si tuvo varias críticas negativas, como el que no era lo suficientemente bueno como para ser llamado un Mario en 3D o que su predecesor era mucho mejor y que jamás podría estar a la altura.</p>


            <h2 class="subtitulo">La innovación de Super Mario Sunshine</h2>

            <img src="./assets/images/MarioSunshine.png">

            <p>A nadie le gustó que cambiaran mucho las mecánicas del juego, empezando porque ahora tenemos una nueva característica con FLUDD que es el invento del profesor Fesor y con él podemos alargar los saltos de Mario a través de la propulsión de agua, podemos disparar chorros de agua y muchas cosas más, muchas personas piensan que esto se sale de los canones del plataformeo, pero sinceramente supieron aprovechar todos los recursos que le planteaba la narrativa y el ambiente, ya que esta historia se desenvuelve en isla Delfino, isla paradisiaca en el que Mario, Peach y los demás se iban a tomar unas vacaciones, pero una versión oscura de Mario, Shadow Mario estaba causando estragos en toda la isla, robando el poder de los Shines y pintarrajeando todo a su alrededor, Mario fue detenido apenas llegar a la isla, solo porque pensaron que fue el perpetrador de dicho suceso, cosa que no fue así ya que Mario a duras penas estaba llegando a la isla, a pesar de eso, fue encarcelado y enjuiciado a tener que limpiar todos los desastres que hizo Shadow Mario para poder tener libertad.
            </p>

            <img src="./assets/images/MecanicaDispararAgua.png">

            <p> Este es un tema un tanto complejo que Nintendo nunca antes se había arriesgado a tomar, y era la de poner una historia con una narrativa y una trama un tanto más elaborada y atrapante, una historia que toca temas (Claro de una forma un poco superficial) como la contaminación, la discriminación, mayor interacción con los NPC's.</p>

            <p>Todo esto fue gracias al apoyo y hecho por mano de Yoshiaki Koizumi, él tenía la idea de que había que poner la historia con temas mucho más profundos, cosa que a Miyamoto no le parecía de un todo bien, puesto que él pensaba que Mario necesitaba tener una visión general un tanto simple y centrarse mucho más en la jugabilidad y gráficos. Entre ambos se llevó una relación de tolerancia y respeto, y gracias a un acuerdo pudieron equilibrar las cosas para que el juego fuera lo que conocemos hoy en día</p>

            <img src="./assets/images/MecanicaVolarAgua.png">

            <p>Hay que admitir que en Mario Sunshine se superaron la mayoría de mecánicas que te ofrecía el Mario 64. Y claro Mario 64 es un juegazo, pero es algo que sin duda, quedará en nuestros recuerdos como uno de los mejores juegos de la época.</p>

            <p>Al final, es una pena que para ese entonces no se haya llevado el mérito que le correspondía por ser uno de los mejores juegos de su época, pero al son del día de hoy, por lo menos ha sido reconocido por muchos como uno de los mejores juegos de Mario. Sobre todo por su entrega en la switch, que está aún más pulida de lo que nos ofrecían en la Game Cube. Es super Mario Sunshine sin duda un juego que se lleva un puesto en uno de los mejores juegos de Mario, por creatividad e innovación que se nos presenta. </p>

            
                 
