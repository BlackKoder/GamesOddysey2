            <h1>La triste verdad de Ocarina of Time</h1>

            <p>A lo largo de los años muchos hemos visto como un juego como el Ocarina of Time, ha traído muchos momentos grandiosos, y horas de juego implacables para muchas personas. Muchos lo recordamos como un juego que goza de una alegría en un mundo de fantasía que nos presenta la idea de salvar un pueblo que necesita la ayuda del héroe. Todos hemos visto a Link como un personaje imparable, como alguien sagaz que puede con cualquier adversidad que se le presenta. <br><br> Para muchos ha sido el ídolo de la infancia por las hazañas que ha conseguido y por no rendirse a todos los males que se le presenta, pero nunca nos hemos puesto a pensar cómo se siente realmente nuestro héroe. Puesto que, en toda la aventura de cualquier juego de la franquicia, vemos que se desenvuelva o se desarrolle una personalidad en Link, en la que podamos saber qué es lo que piensa o quiere decir en cierto momento.</p>

            <img src="./assets/images/ocarina-triste.jpg" alt="Link y Navy en el campo de Hyrule con la Ocarina de Saria" title="Link y Navy en el campo de Hyrule con la Ocarina de Saria">

            <p>Bien sabemos que esto se deba a que Nintendo ha buscado de generar cierto "enlace" entre el jugador y Link para que el jugador sienta que pertenece a la aventura, o que realmente tiene cierta inmersión por así decirlo, pero tenemos que reconocer que a pesar de que los juegos de Zelda muchos traten de brindarnos una experiencia de mundo abierto, o que somos los que tenemos el poder de elegir cualquier paso de la historia, siempre va a existir el detalle de que es un juego con una historia un poco lineal. Ya que la historia viene fija, las acciones de los personajes están establecidas, todo está decidido para mostrarnos un final en la aventura que emprendemos, sobre todo, nuestro héroe en este caso también tiene un mismo final. <br><br> Y honestamente no es el mejor final para quien buscó de liberar Hyrule de las tinieblas. A lo largo de la aventura vemos como la trama, cuando Link es un niño, es muy alegre, pero después de que nos hacemos adultos hay que reconocer que las cosas giran en torno a algo un poco más gris y oscuro. Para Link llevar con el peso de tener que salvar Hyrule de la noche a la mañana, siendo aun así un niño es algo muy delicado, o sea, pensémoslo bien.</p>

            <img src="./assets/images/link-llorando.png" alt="Mido llorando de tristeza y el reencuentro de Link con los Kokiri" title="Mido llorando de tristeza y el reencuentro de Link con los Kokiri">

                <p>Link se había criado en el bosque Kokiri como uno de ellos por el gran Árbol Deku, por toda su vida él pensó que era un Kokiri haciendo su vida, sus amigos y viendo al gran árbol Deku como una figura paterna. Pero tuvo que ver como quien lo crío durante todo ese tiempo parte a mejor vida, y teniendo encomendada la misión con un gran peso de conseguir las tres piedras espirituales de la trifuerza cuando nunca había salido de ahí era grande, sobre todo por no haber explorado nunca un sitio tan extenso como lo es Hyrule, al emprender su viaje deja atrás a Saria quien era su mejor amiga quien acompañó durante todo ese tiempo. <br><br>Tiene que lidiar ir a sitios lúgubres y peligrosos a los que no debería de ir un niño, y aun así no tiene otra opción más que hacerlo. Puesto que no tiene otra elección de igual forma, al conseguir reunir los fragmentos de la trifuerza, y sacar la espada maestra de su pedestal, se quedó atrapado unos 7 años hasta que se volviera adulto para poder utilizar dicha espada. Pensar que perdió 7 años de su vida y en un abrir y cerrar de ojos vio como ha cambiado todo, desde su cuerpo hasta todo lo que había a sus alrededores, como las personas que había conocido en su aventura. Y como Hyrule había sido destruido. Igualmente, tiene que enfrentarse a adversidades y enemigos mucho más peligrosos que los que se había enfrentado antes. </p>

                <img src="./assets/images/deku-tree.jpg" alt="Link frente al gran árbol deku con la espada y escudo kokiri" title="Link frente al gran árbol deku con la espada y escudo kokiri">

                <p>Al terminar su hazaña en el templo del bosque se entera de muchas cosas que igual no le pudieron haber venido bien a nuestro héroe. Como el que Saria su amiga de la infancia, haya tenido que dar su vida para convertirse en la sabia del bosque. O como la verdad que le cuenta el retoño del gran Árbol Deku, sobre su historia y su pasado. De la época de las guerras civiles en Hyrule donde descubre que su mamá lo dejó en el bosque Kokiri a manos del gran Árbol Deku para escapar y mantenerlo a salvo, saber que su mamá no pudo sobrevivir y que nada de lo que había vivido durante todo ese tiempo tenía sentido, puesto que carecía de una identidad como tal, era algo que podría ser muy duro para Link.</p>

                <p>Pero lo que diría que más daño pudo haberle hecho fue el hecho de que al final del juego Zelda lo regresara 7 años al pasado para que recuperara eso que no pudo vivir por haberse mantenido encerrado por los poderes de la espada maestra. Hay que tener en cuenta que lo que hizo Zelda en sí fue un error, puesto que Link ya no tenía un lugar al cual regresar y llamar hogar, ya no poseía una familia ni amigos, sobre todo al haber hecho esto ya nadie había reconocido su gran hazaña por salvar Hyrule y absolutamente nada de lo que hizo tuvo algún significado para nadie porque nadie sabe de su existencia, nadie sabe quién es el héroe del tiempo y está obligado a vagar solo sin rumbo en la búsqueda de su compañera Navy, quien lo acompañó en su aventura, pero una vez regresaron al pasado se marchó sin decir más... <br><br>Ya Link no tenía una identidad, no tenía a nadie y no tenía el mérito de haber sido el héroe del tiempo. La única persona que podía considerar como cercana se alejó de él sin decir nada. Y terminó vagando solo en el bosque de los perdidos hasta los sucesos de Majoras Mask. </p>

                <img src="./assets/images/sombra-heroe.png" alt="La Sombra del Héroe del Tiempo en Twilight Princess" title="La Sombra del Héroe del Tiempo en Twilight Princess">

                <p>Todo hasta convertirse en un Estalfos, al final no sabemos exactamente cómo pasa a morir el héroe del tiempo, es un poco incierto su destino, pero lo que sí podemos saber es que en el libro de Hyrule Historia nos dice que el héroe del tiempo se convirtió en un estalfos y es quien le enseña al Link de Twilight Princess como luchar en combate. <br><br>Expresandole lo mucho que se arrepentía de no poder haberle enseñado a nadie su técnica con la espada y que nadie lo recordara de ninguna forma... Haber quedado atrapado en una brecha del tiempo donde todo se convierte en un bucle sin fin de haber hecho y no haber hecho nada a la vez es algo que puede dejar secuelas traumáticas para nuestro héroe. Y sobre todo un héroe sin gloria es como una espada sin filo. Ese fue el crudo destino para Link en Ocarina of Time, pero para nosotros siempre estará en nuestros corazones como el verdadero héroe del tiempo.</p>
