            <p>Igualmente, podemos apreciar que el punto de referencia donde vamos a hacer las primeras misiones del juego es en el apartamento de Sora, que se encuentra específicamente en Minami-Apyoma. Posiblemente, una referencia a Minami-Aoyama, un barrio de Tokyo que se encuentra al noroeste de Minato, y específicamente es Minami-Aoyama es la zona sur del barrio de Aoyama, podría ser una referencia, pero el caso es que está más que claro que se basaron en la verdadera ciudad de Tokyo para hacer los escenarios y mapas del Kingdom Hearts IV. Ya solo nos queda ver cuando esté en su lanzamiento cómo serán las cosas.</p>

