            <h1>¿Es realmente Ocarina of Time un juego avanzado para su época?</h1>

            <img src="./assets/images/avanzado-ocarina.png" alt="¿Es realmente Ocarina of Time un juego avanzado para su época?" title="Imágen de ¿Es realmente Ocarina of Time un juego avanzado para su época?">

            <p>¿Cuántos que hayan jugado el juego en su infancia lo recuerda como un mal juego?, por donde lo veas es un juego que ha sido una obra maestra y que marcó un hito en la etapa de los videojuegos en el salto al 3D. <br><br>Es cierto que ya había prototipos de otros juegos que querían dar el salto al 3D en esa época, sin embargo, nunca se hubiera implementado cada una de las mecánicas y libertad que se planteó en el Ocarina of Time. Ni siquiera en el Mario 64, ya que este tenía ciertas limitaciones en cuánto a la libertad, jugabilidad y mecánicas. Que, sin embargo, también fue un juego muy representativo y fue el punto de partida de creación de Zelda Ocarina of Time, puesto que esté poseía las mecánicas base que se implementarían en el mundo del juego para que se viera más vivo y gozara de más libertad. Igualmente con el tema de la optimización y el rendimiento.</p>

            <h2 class="subtitulo">Historia y narrativa</h2>

            <img src="./assets/images/historia-ocarina.png" alt="Historia y narrativa de The Legend of Zelda Ocarina of Time | Link vs Ganondorf batalla final" title="Imágen de Historia y narrativa de The Legend of Zelda Ocarina of Time">

            <p>Zelda en ese tiempo poseía una narrativa y una filosofía que quizás en ese tiempo no hubiera sido muy entendida, ya que la mayoría de las atracciones principales estaban en la interacción del jugador con el mundo y los personajes del juego, la jugabilidad y la estética es lo que marcaba la importancia en el desarrollo de Nintendo para ese juego. Sin embargo, eso no quiere decir que el juego no esté bien estructurado en sus guiones o historias. <br><br>Ha cumplido con muchas otras cosas que otros juegos no contaban para su época. Desde la aparición de líneas de tiempo que crearan una bifurcación en toda la cronología de la saga y haciendo que un juego se pudiera conectar en cuánto a historia de una u otra forma o incluso llegando a hacer referencias entre un juego y otro en algunos casos.</p>

                <h2 class="subtitulo">Arte y diseño del juego</h2>

                <img src="./assets/images/artwork-ocarina.jpg" alt="Arte y diseño del juego de The Legend of Zelda Ocarina of Time | Link cabalgando con Epona en el bosque de los pérdidos">

                <p>El arte y diseño del juego estaban construidos precisamente para hacer que el jugador se viera arraigado al ambiente de ese mundo, que realmente gozara de esa libertad y se sintiera que estaba en un mundo que realmente estaba vivo. Quisieron innovar con algunos aspectos de los personajes, cambiando así el diseño de Link para que se viera mucho más atractivo a la vista de los jugadores, los ambientes, todos los lugares y cosas coloridas que supieron incluir a la narrativa del juego fue un gran recurso para proyectarle a uno una gran aventura épica. <br><br>Incluso el juego contaba con mejores gráficos que algunos juegos que hayan salido para la misma consola, como en el caso de Resident Evil 2, que al final tuvo que hacer un salto a la consola de la PlayStation porque no podían hacer que esos diseños concordarán con el procesamiento del hardware de la consola. Cosa que no pasa en el Ocarina, muchos podrían pensar que posiblemente sea porque los diseños de los personajes de Resident Evil buscaban de dar un aire más realista y hacer muchos detalles a un diseño poligonal terminó haciendo algo desalentador.</p>

                <img src="./assets/images/artwork-ocarina-batalla.jpeg" alt="Arte y diseño del juego de The Legend of Zelda Ocarina of Time | Batalla de Hyrule contra las fuerzas del mal de Ganon">

                <p>Pero eso no quiere decir que haya sido un mal juego, al contrario, Resident Evil 2 también fue un buen juego para su época, pero precisamente Nintendo 64 no fue la mejor consola que hizo que destacara entre otros juegos. Cosa que en Ocarina of Time no pasó, ya que Nintendo se centró más el diseño de un juego que tuviera las características bien a medida para esa consola, y qué mejor juego que el icónico Ocarina of Time?. De hecho, hago hincapié en que Mario fue una gran referencia para el diseño de gran parte de los mundos de Zelda por el hecho de que le dio ese ambiente de fantasía y mundo colorido, pero luego le dieron ese toque o trasfondo más filosófico que pudo atrapar a muchos jugadores de la saga.</p>

                <h2 class="subtitulo">Mecánicas y jugabilidad</h2>

                <img src="./assets/images/jugabilidad-ocarina.png" alt="Mecánicas y jugabilidad de The Legend of Zelda Ocarina of Time | Link apuntando con el gancho al roble de la entrada al Templo del Bosque" title="Mecánicas y jugabilidad de The Legend of Zelda Ocarina of Time | Link apuntando con el gancho al roble de la entrada al Templo del Bosque">

                <p>Es cierto que la Nintendo 64 fue una consola revolucionaria para su época antes de que saliera la PlayStation de Sony. Ya que le dio un gran golpe a las antiguas consolas de Sega e innovó y mejoró muchos aspectos de las características de mundo 3D de los videojuegos. Claro que no tuviera el mismo tipo de éxito económico que la competencia, bien se debe a los cartuchos, porque hacían el coste de producción más caro, pero eso no quita que fuera un invento revolucionario. <br><br>Es por eso que cuando decidieron ponerse manos a la obra con el título de Ocarina of Time, buscaron de aprovechar y sacarle el máximo potencial a todo el hardware, ya que la comodidad y jugabilidad se enfoca en la comodidad de sus controles y la practicidad que te dan por ejemplo con los botones C, para que pudieras predefinir los objetos que ibas a tener en la aventura, o con el sistema de Z-targeting que integraría Nintendo para el juego, fueron ideas que mejoraron por ejemplo de lo que aprendieron de Mario 64, ya que se habían dado cuenta de lo difícil que se hacía derrotar algunos enemigos con la gravedad que se había programado en el juego y en Ocarina decidieron mejorar varios aspectos de la cámara para que no pasaras por los mismos problemas que en Mario 64.</p>

                <img src="./assets/images/z-targeting.png" alt="Mecánicas y jugabilidad de The Legend of Zelda Ocarina of Time | Link dando un salto hacia atrás mientras la mira del z-targeting está activada" title="Mecánicas y jugabilidad de The Legend of Zelda Ocarina of Time | Link dando un salto hacia atrás mientras la mira del z-targeting está activada">

                <p>Pero no solo eso, tenemos que admitir que las mecánicas que lograron implementar en los juegos eran realmente superiores a los demás juegos que estaban contemporáneos e incluso los pasados. El juego te da tanta libertad que te deja montar a caballo, tiene su sistema de comercio, puedes pescar, trepar, combatir, existe la transición del día y la noche, la fijación del objetivo en el combate, puedes tocar un instrumento, explorar un mundo amplio, cambiar la cámara a primera persona, puedes utilizar muchos tipos de armas como lo son el gancho y el arco y flecha, destruir elementos del escenario con las bombas, tienes un mapa para guiarte en la zona donde estés. Realmente es una basta variedad de elementos que lo convirtieron en el juegazo de su año.</p>

                <img src="./assets/images/jugabilidad2-ocarina.png" alt="Mecánicas y jugabilidad de The Legend of Zelda Ocarina of Time | Link apuntando a un ojo del templo del bosque con el arco y flecha" title="Mecánicas y jugabilidad de The Legend of Zelda Ocarina of Time | Link apuntando a un ojo del templo del bosque con el arco y flecha">

                <h2 class="subtitulo">La Música</h2>

                <img src="./assets/images/musica-ocarina.png" alt="La Música de The Legend of Zelda Ocarina of Time | Concierto musical de Link y Sheik Ocarina of Time">

                <p>Realmente tiene una gran profundidad la mayoría de las bandas sonoras que se crearon para la mayoría de momentos del juego, los temas dedicados a algún personaje en específico, realmente la música fue algo que el compositor Koji Kondo supo manejar para que cuadrara con la narrativa la jugabilidad del juego y que fuera muy acertada con las situaciones del juego, igualmente que pudiera ser soportada en el hardware de la Nintendo 64, un gran desafío, ya que para ese entonces buscaban de hacer hasta lo imposible para la optimización del juego y fue logrado junto con los demás elementos implementarlos en el cartucho de su época.</p>

                <p>Entonces, al final, seguirá marcando un punto de referencia para la mayoría de juegos de esta época y podemos denotar que todos los patrones que ha seguido Ocarina of Time a lo largo de su travesía por querer convertirse en uno de los mejores juegos lo ha convertido realmente en una leyenda.</p>
