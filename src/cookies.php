			<h2 class="subtitulo">QUÉ ES UNA COOKIE</h2>

            <p>Una cookie es un fichero que se descarga en tu ordenador al acceder a
			determinadas páginas web o blogs.
			Las cookies permiten a esa página, entre otras cosas, almacenar y recuperar
			información sobre tus hábitos de navegación o de tu equipo, y dependiendo de
			la información que contengan y de la forma en que utilices tu equipo, pueden
			utilizarse para reconocerte.
			<br><br>
			El navegador del usuario memoriza cookies en el disco duro solamente durante
			la sesión actual ocupando un espacio de memoria mínimo y no perjudicando al
			ordenador. Las cookies no contienen ninguna clase de información personal
			específica, y la mayoría de las mismas se borran del disco duro al finalizar la
			sesión de navegador (las denominadas cookies de sesión).
			<br><br>
			La mayoría de los navegadores aceptan como estándar a las cookies y, con
			independencia de las mismas, permiten o impiden en los ajustes de seguridad
			las cookies temporales o memorizadas.
			<br><br>
			Las cookies se asocian al navegador, no a la persona, por lo que no suelen
			almacenar información sensible sobre ti como tarjetas de crédito o datos
			bancarios, fotografías o información personal, etc. Los datos que guardan son
			de carácter técnico, estadísticos, preferencias personales, personalización de
			contenidos, etc.</p>

